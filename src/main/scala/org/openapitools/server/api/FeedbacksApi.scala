class FeedbacksApi(
    feedbacksService: FeedbacksApiService,
    feedbacksMarshaller: FeedbacksApiMarshaller
) {

  
  import feedbacksMarshaller._

  lazy val route: Route =
    path("campaigns" / LongNumber / "feedback" / "updates") { (campaignId) => 
      get { 
        parameters("page_token".as[String].?, "limit".as[Int].?, "from_date".as[String].?) { (pageToken, limit, fromDate) => 
            feedbacksService.getFeedbackAndCommentUpdates(campaignId = campaignId, pageToken = pageToken, limit = limit, fromDate = fromDate)
        }
      }
    }
}



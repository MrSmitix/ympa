class ModelsApi(
    modelsService: ModelsApiService,
    modelsMarshaller: ModelsApiMarshaller
) {

  
  import modelsMarshaller._

  lazy val route: Route =
    path("models" / LongNumber) { (modelId) => 
      get { 
        parameters("regionId".as[Long], "currency".as[String].?) { (regionId, currency) => 
            modelsService.getModel(modelId = modelId, regionId = regionId, currency = currency)
        }
      }
    } ~
    path("models" / LongNumber / "offers") { (modelId) => 
      get { 
        parameters("regionId".as[Long], "currency".as[String].?, "orderByPrice".as[String].?, "count".as[Int].?(10), "page".as[Int].?(1)) { (regionId, currency, orderByPrice, count, page) => 
            modelsService.getModelOffers(modelId = modelId, regionId = regionId, currency = currency, orderByPrice = orderByPrice, count = count, page = page)
        }
      }
    } ~
    path("models") { 
      post { 
        parameters("regionId".as[Long], "currency".as[String].?) { (regionId, currency) => 
            entity(as[GetModelsRequest]){ getModelsRequest =>
              modelsService.getModels(regionId = regionId, getModelsRequest = getModelsRequest, currency = currency)
            }
        }
      }
    } ~
    path("models" / "offers") { 
      post { 
        parameters("regionId".as[Long], "currency".as[String].?, "orderByPrice".as[String].?) { (regionId, currency, orderByPrice) => 
            entity(as[GetModelsRequest]){ getModelsRequest =>
              modelsService.getModelsOffers(regionId = regionId, getModelsRequest = getModelsRequest, currency = currency, orderByPrice = orderByPrice)
            }
        }
      }
    } ~
    path("models") { 
      get { 
        parameters("query".as[String], "regionId".as[Long], "currency".as[String].?, "page".as[Int].?(1), "pageSize".as[Int].?) { (query, regionId, currency, page, pageSize) => 
            modelsService.searchModels(query = query, regionId = regionId, currency = currency, page = page, pageSize = pageSize)
        }
      }
    }
}



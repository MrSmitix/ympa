class OrderLabelsApi(
    orderLabelsService: OrderLabelsApiService,
    orderLabelsMarshaller: OrderLabelsApiMarshaller
) {

  
  import orderLabelsMarshaller._

  lazy val route: Route =
    path("campaigns" / LongNumber / "orders" / LongNumber / "delivery" / "shipments" / LongNumber / "boxes" / LongNumber / "label") { (campaignId, orderId, shipmentId, boxId) => 
      get { 
        parameters("format".as[String].?) { (format) => 
            orderLabelsService.generateOrderLabel(campaignId = campaignId, orderId = orderId, shipmentId = shipmentId, boxId = boxId, format = format)
        }
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "delivery" / "labels") { (campaignId, orderId) => 
      get { 
        parameters("format".as[String].?) { (format) => 
            orderLabelsService.generateOrderLabels(campaignId = campaignId, orderId = orderId, format = format)
        }
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "delivery" / "labels" / "data") { (campaignId, orderId) => 
      get {  
            orderLabelsService.getOrderLabelsData(campaignId = campaignId, orderId = orderId)
      }
    }
}



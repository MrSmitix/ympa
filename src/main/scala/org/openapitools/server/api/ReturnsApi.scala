class ReturnsApi(
    returnsService: ReturnsApiService,
    returnsMarshaller: ReturnsApiMarshaller
) {

  
  import returnsMarshaller._

  lazy val route: Route =
    path("campaigns" / LongNumber / "orders" / LongNumber / "returns" / LongNumber) { (campaignId, orderId, returnId) => 
      get {  
            returnsService.getReturn(campaignId = campaignId, orderId = orderId, returnId = returnId)
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "returns" / LongNumber / "application") { (campaignId, orderId, returnId) => 
      get {  
            returnsService.getReturnApplication(campaignId = campaignId, orderId = orderId, returnId = returnId)
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "returns" / LongNumber / "decision" / LongNumber / "image" / Segment) { (campaignId, orderId, returnId, itemId, imageHash) => 
      get {  
            returnsService.getReturnPhoto(campaignId = campaignId, orderId = orderId, returnId = returnId, itemId = itemId, imageHash = imageHash)
      }
    } ~
    path("campaigns" / LongNumber / "returns") { (campaignId) => 
      get { 
        parameters("page_token".as[String].?, "limit".as[Int].?, "orderIds".as[String].?, "statuses".as[String].?, "type".as[String].?, "fromDate".as[String].?, "toDate".as[String].?, "from_date".as[String].?, "to_date".as[String].?) { (pageToken, limit, orderIds, statuses, `type`, fromDate, toDate, fromDate2, toDate2) => 
            returnsService.getReturns(campaignId = campaignId, pageToken = pageToken, limit = limit, orderIds = orderIds, statuses = statuses, `type` = `type`, fromDate = fromDate, toDate = toDate, fromDate2 = fromDate2, toDate2 = toDate2)
        }
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "returns" / LongNumber / "decision") { (campaignId, orderId, returnId) => 
      post {  
            entity(as[SetReturnDecisionRequest]){ setReturnDecisionRequest =>
              returnsService.setReturnDecision(campaignId = campaignId, orderId = orderId, returnId = returnId, setReturnDecisionRequest = setReturnDecisionRequest)
            }
      }
    } ~
    path("campaigns" / LongNumber / "orders" / LongNumber / "returns" / LongNumber / "decision" / "submit") { (campaignId, orderId, returnId) => 
      post {  
            returnsService.submitReturnDecision(campaignId = campaignId, orderId = orderId, returnId = returnId)
      }
    }
}



class OutletLicensesApi(
    outletLicensesService: OutletLicensesApiService,
    outletLicensesMarshaller: OutletLicensesApiMarshaller
) {

  
  import outletLicensesMarshaller._

  lazy val route: Route =
    path("campaigns" / LongNumber / "outlets" / "licenses") { (campaignId) => 
      delete { 
        parameters("ids".as[String].?) { (ids) => 
            outletLicensesService.deleteOutletLicenses(campaignId = campaignId, ids = ids)
        }
      }
    } ~
    path("campaigns" / LongNumber / "outlets" / "licenses") { (campaignId) => 
      get { 
        parameters("outletIds".as[String].?, "ids".as[String].?) { (outletIds, ids) => 
            outletLicensesService.getOutletLicenses(campaignId = campaignId, outletIds = outletIds, ids = ids)
        }
      }
    } ~
    path("campaigns" / LongNumber / "outlets" / "licenses") { (campaignId) => 
      post {  
            entity(as[UpdateOutletLicenseRequest]){ updateOutletLicenseRequest =>
              outletLicensesService.updateOutletLicenses(campaignId = campaignId, updateOutletLicenseRequest = updateOutletLicenseRequest)
            }
      }
    }
}



     * Creates the codec for converting ReturnDecisionType from and to JSON.
     */
    implicit val decoder: Decoder[ReturnDecisionType] = deriveDecoder
    implicit val encoder: ObjectEncoder[ReturnDecisionType] = deriveEncoder

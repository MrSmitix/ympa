/**
* Тип атрибуции:   * `CLICKS` — по кликам.   * `SHOWS` — по показам. <br><br>  О том, какие данные в отчете зависят и не зависят от типа атрибуции, читайте [в Справке Маркета для продавцов](https://yandex.ru/support2/marketplace/ru/marketing/shelf#stats). 
* Values: CLICKS,SHOWS
*/
enum class ShelfsStatisticsAttributionType(@get:JsonValue val value: kotlin.String) {

    CLICKS("CLICKS"),
    SHOWS("SHOWS");

    companion object {
        @JvmStatic
        @JsonCreator
        fun forValue(value: kotlin.String): ShelfsStatisticsAttributionType {
                return values().first{it -> it.value == value}
        }
    }
}


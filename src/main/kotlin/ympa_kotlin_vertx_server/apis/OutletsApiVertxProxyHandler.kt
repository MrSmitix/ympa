import com.google.gson.Gson
import ympa_kotlin_vertx_server.models.ApiClientDataErrorResponse
import ympa_kotlin_vertx_server.models.ApiForbiddenErrorResponse
import ympa_kotlin_vertx_server.models.ApiLimitErrorResponse
import ympa_kotlin_vertx_server.models.ApiNotFoundErrorResponse
import ympa_kotlin_vertx_server.models.ApiServerErrorResponse
import ympa_kotlin_vertx_server.models.ApiUnauthorizedErrorResponse
import ympa_kotlin_vertx_server.models.ChangeOutletRequest
import ympa_kotlin_vertx_server.models.CreateOutletResponse
import ympa_kotlin_vertx_server.models.EmptyApiResponse
import ympa_kotlin_vertx_server.models.GetOutletResponse
import ympa_kotlin_vertx_server.models.GetOutletsResponse

class OutletsApiVertxProxyHandler(private val vertx: Vertx, private val service: OutletsApi, topLevel: Boolean, private val timeoutSeconds: Long) : ProxyHandler() {
    private lateinit var timerID: Long
    private var lastAccessed: Long = 0
    init {
        try {
            this.vertx.eventBus().registerDefaultCodec(ServiceException::class.java,
            ServiceExceptionMessageCodec())
        } catch (ex: IllegalStateException) {}

        if (timeoutSeconds != (-1).toLong() && !topLevel) {
            var period = timeoutSeconds * 1000 / 2
            if (period > 10000) {
                period = 10000
            }
            this.timerID = vertx.setPeriodic(period) { this.checkTimedOut(it) }
        } else {
            this.timerID = -1
        }
        accessed()
    }
    private fun checkTimedOut(id: Long) {
        val now = System.nanoTime()
        if (now - lastAccessed > timeoutSeconds * 1000000000) {
            close()
        }
    }

    override fun close() {
        if (timerID != (-1).toLong()) {
            vertx.cancelTimer(timerID)
        }
        super.close()
    }

    private fun accessed() {
        this.lastAccessed = System.nanoTime()
    }
    override fun handle(msg: Message<JsonObject>) {
        try {
            val json = msg.body()
            val action = msg.headers().get("action") ?: throw IllegalStateException("action not specified")
            accessed()
            val contextSerialized = json.getJsonObject("context") ?: throw IllegalStateException("Received action $action without OperationRequest \"context\"")
            val context = OperationRequest(contextSerialized)
            when (action) {
        
                "createOutlet" -> {
                    val params = context.params
                    val campaignId = ApiHandlerUtils.searchLongInJson(params,"campaignId")
                    if(campaignId == null){
                        throw IllegalArgumentException("campaignId is required")
                    }
                    val changeOutletRequestParam = ApiHandlerUtils.searchJsonObjectInJson(params,"body")
                    if (changeOutletRequestParam == null) {
                        throw IllegalArgumentException("changeOutletRequest is required")
                    }
                    val changeOutletRequest = Gson().fromJson(changeOutletRequestParam.encode(), ChangeOutletRequest::class.java)
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.createOutlet(campaignId,changeOutletRequest,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
                        msg.reply(res.toJson())
                    }.invokeOnCompletion{
                        it?.let{ throw it }
                    }
                }
        
                "deleteOutlet" -> {
                    val params = context.params
                    val campaignId = ApiHandlerUtils.searchLongInJson(params,"campaignId")
                    if(campaignId == null){
                        throw IllegalArgumentException("campaignId is required")
                    }
                    val outletId = ApiHandlerUtils.searchLongInJson(params,"outletId")
                    if(outletId == null){
                        throw IllegalArgumentException("outletId is required")
                    }
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.deleteOutlet(campaignId,outletId,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
                        msg.reply(res.toJson())
                    }.invokeOnCompletion{
                        it?.let{ throw it }
                    }
                }
        
                "getOutlet" -> {
                    val params = context.params
                    val campaignId = ApiHandlerUtils.searchLongInJson(params,"campaignId")
                    if(campaignId == null){
                        throw IllegalArgumentException("campaignId is required")
                    }
                    val outletId = ApiHandlerUtils.searchLongInJson(params,"outletId")
                    if(outletId == null){
                        throw IllegalArgumentException("outletId is required")
                    }
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.getOutlet(campaignId,outletId,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
                        msg.reply(res.toJson())
                    }.invokeOnCompletion{
                        it?.let{ throw it }
                    }
                }
        
                "getOutlets" -> {
                    val params = context.params
                    val campaignId = ApiHandlerUtils.searchLongInJson(params,"campaignId")
                    if(campaignId == null){
                        throw IllegalArgumentException("campaignId is required")
                    }
                    val pageToken = ApiHandlerUtils.searchStringInJson(params,"page_token")
                    val regionId = ApiHandlerUtils.searchLongInJson(params,"region_id")
                    val shopOutletCode = ApiHandlerUtils.searchStringInJson(params,"shop_outlet_code")
                    val regionId2 = ApiHandlerUtils.searchLongInJson(params,"regionId")
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.getOutlets(campaignId,pageToken,regionId,shopOutletCode,regionId2,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
                        msg.reply(res.toJson())
                    }.invokeOnCompletion{
                        it?.let{ throw it }
                    }
                }
        
                "updateOutlet" -> {
                    val params = context.params
                    val campaignId = ApiHandlerUtils.searchLongInJson(params,"campaignId")
                    if(campaignId == null){
                        throw IllegalArgumentException("campaignId is required")
                    }
                    val outletId = ApiHandlerUtils.searchLongInJson(params,"outletId")
                    if(outletId == null){
                        throw IllegalArgumentException("outletId is required")
                    }
                    val changeOutletRequestParam = ApiHandlerUtils.searchJsonObjectInJson(params,"body")
                    if (changeOutletRequestParam == null) {
                        throw IllegalArgumentException("changeOutletRequest is required")
                    }
                    val changeOutletRequest = Gson().fromJson(changeOutletRequestParam.encode(), ChangeOutletRequest::class.java)
                    GlobalScope.launch(vertx.dispatcher()){
                        val result = service.updateOutlet(campaignId,outletId,changeOutletRequest,context)
                        val payload = JsonObject(Json.encode(result.payload)).toBuffer()
                        val res = OperationResponse(result.statusCode,result.statusMessage,payload,result.headers)
                        msg.reply(res.toJson())
                    }.invokeOnCompletion{
                        it?.let{ throw it }
                    }
                }
        
            }
        }catch (t: Throwable) {
            msg.reply(ServiceException(500, t.message))
            throw t
        }
    }
}
